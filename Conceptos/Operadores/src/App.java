public class App {
    public static void main(String[] args) throws Exception {
        /*
        Aritméticos

         Suma --> *
         Resta --> -
         Multiplicación --> *
         Division --> /
         Resto --> %
        */

        int num1 = 5;
        int num2 = 3;
        int suma, resta, multiplicacion, division, resto;

        suma = num1 + num2;
        resta = num1 - num2;
        multiplicacion = num1 * num2;
        division = num1 / num2;
        resto = num1 % num2;

        System.out.printf("Suma >> %d\n", suma);
        System.out.printf("Resta >> %d\n", resta);
        System.out.printf("Multiplicacion >> %d\n", multiplicacion);
        System.out.printf("Division >> %d\n", division);
        System.out.printf("Resto >> %d\n", resto);

        /*
        Lógicos

         Mayor que --> >
         Mayor o igual que --> >=
         Menor que --> <
         Menor o igual que --> <=
         Igual que --> ==
         Distincto que --> !=
         Negación --> !
         And --> &&
         Or --> ||
        */

        /*
        Asignacion
         
        Igual --> =
        += --> x = x + y;
        -= --> x = x - y;
        *= --> x = x * y;
        /= --> x = x / y;
        %= --> x = x % y;

        Incremento --> ++
        Decremento --> --
        */
    }
}
